"""
Evaluation report generator for MDASH benchmark results.

Produces markdown and JSON reports that can be used for:
- Grant applications (reproducible evidence)
- Publication supplementary materials
- Internal team metrics
- A/B testing different model configurations
"""
import json
import logging
from datetime import datetime
from typing import Dict, List

from .evaluator import EvaluationResult

logger = logging.getLogger(__name__)


class EvaluationReportGenerator:
    """Generate formatted evaluation reports."""

    def __init__(self):
        self.generated_at = datetime.utcnow().isoformat()

    def generate_markdown(self, result: EvaluationResult) -> str:
        """Generate a markdown evaluation report."""
        md = f"""# MDASH Benchmark Evaluation Report

## Dataset
- **Name**: {result.dataset_name}
- **Ground truth vulnerabilities**: {result.total_ground_truth}
- **Evaluation timestamp**: {self.generated_at}

## Summary

| Metric | Value |
|--------|-------|
| True Positives | {result.true_positives} |
| False Positives | {result.false_positives} |
| False Negatives | {result.false_negatives} |
| **Precision** | **{result.precision:.4f}** |
| **Recall** | **{result.recall:.4f}** |
| **F1 Score** | **{result.f1_score:.4f}** |
| False Positive Rate | {result.false_positive_rate:.4f} |

## Per-Category Breakdown

| Category | Ground Truth | TP | FN | FP | Precision | Recall |
|----------|-------------|----|----|----|-----------|--------|
"""
        for cat, stats in result.category_results.items():
            md += f"| {cat} | {stats['ground_truth']} | {stats['true_positives']} | {stats['false_negatives']} | {stats['false_positives']} | {stats['precision']:.4f} | {stats['recall']:.4f} |\n"

        md += f"""
## Matched Findings

"""
        for match in result.matches:
            md += f"""### {match.ground_truth.vuln_id}: {match.ground_truth.description}
- **Category**: {match.ground_truth.category.value}
- **Location**: {match.ground_truth.location}
- **CWE**: {match.ground_truth.cwe_id or 'N/A'}
- **Match type**: {match.match_type}
- **Confidence**: {match.confidence:.2f}
- **Pipeline location**: {match.pipeline_finding.proven_finding.deduplicated_finding.representative_finding.original_candidate.location if match.pipeline_finding else 'N/A'}

"""

        if result.false_negatives > 0:
            md += "\n## Missed Vulnerabilities (False Negatives)\n\n"
            matched_ids = {m.ground_truth.vuln_id for m in result.matches}
            # We don't have access to the dataset here, but in practice we'd list them
            md += f"*{result.false_negatives} ground-truth vulnerabilities were not matched by any pipeline finding.*\n\n"

        if result.false_positives > 0:
            md += f"\n## False Positives\n\n*{result.false_positives} pipeline findings did not match any ground-truth vulnerability.*\n\n"

        md += f"""## Pipeline Configuration
```json
{json.dumps(result.pipeline_config, indent=2)}
```

## Cost & Performance
- Wall clock time: {result.wall_clock_seconds:.1f}s
- Total cost: ${result.total_cost_usd:.2f} USD

---
*Generated by MDASH Evaluation Framework*
"""
        return md

    def generate_json(self, result: EvaluationResult) -> str:
        """Generate a JSON evaluation report."""
        data = {
            "report_type": "mdash_benchmark_evaluation",
            "generated_at": self.generated_at,
            **result.to_dict()
        }
        return json.dumps(data, indent=2)

    def generate_grant_summary(self, result: EvaluationResult) -> str:
        """
        Generate a concise summary suitable for grant applications.
        Emphasizes reproducibility and the unseen-code property.
        """
        return f"""## MDASH Benchmark Result: {result.dataset_name}

**Dataset**: {result.dataset_name} — {result.total_ground_truth} deliberately injected vulnerabilities in private codebase (never published, not in LLM training data).

**Result**: {result.true_positives}/{result.total_ground_truth} vulnerabilities correctly identified, {result.false_positives} false positives.

**Metrics**:
- Precision: {result.precision:.2%}
- Recall: {result.recall:.2%}
- F1 Score: {result.f1_score:.2%}

**Evidence of genuine reasoning**: Because the target codebase is private and was never included in public LLM training corpora, all correct findings are evidence of the pipeline's vulnerability discovery capabilities rather than pattern matching from memorized training examples.

**Reproducibility**: Full evaluation artifacts, including pipeline configuration, model selections, debate transcripts, and sanitizer outputs, are preserved in the reproducibility kit.
"""

    def generate_ab_comparison(
        self,
        results: List[EvaluationResult],
        labels: List[str]
    ) -> str:
        """Generate an A/B comparison report for multiple configurations."""
        md = "# MDASH A/B Comparison Report\n\n"
        md += "| Configuration | TP | FP | FN | Precision | Recall | F1 | Cost |\n"
        md += "|-------------|----|----|----|-----------|--------|----|------|\n"

        for result, label in zip(results, labels):
            md += (
                f"| {label} | {result.true_positives} | {result.false_positives} | "
                f"{result.false_negatives} | {result.precision:.4f} | "
                f"{result.recall:.4f} | {result.f1_score:.4f} | "
                f"${result.total_cost_usd:.2f} |\n"
            )

        # Find best configuration
        best_f1 = max(results, key=lambda r: r.f1_score)
        best_precision = max(results, key=lambda r: r.precision)
        best_recall = max(results, key=lambda r: r.recall)

        md += f"""
## Best Configuration by Metric
- **Highest F1**: {labels[results.index(best_f1)]} ({best_f1.f1_score:.4f})
- **Highest Precision**: {labels[results.index(best_precision)]} ({best_precision.precision:.4f})
- **Highest Recall**: {labels[results.index(best_recall)]} ({best_recall.recall:.4f})
"""
        return md
